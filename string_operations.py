def reverse_string(s):
    return s[: : -1]
def capitalize_string(s):
    return s.capitalize()
def lowercase_string(s):
    return s.lower()
def uppercase_string(s):
    return s.upper()
print(reverse_string("Pirawas"))
print(capitalize_string("pirawas"))
print(lowercase_string("PIRAWAS"))
print(uppercase_string("pirawas"))